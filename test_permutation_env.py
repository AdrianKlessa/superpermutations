import unittest
import permutation_env
import numpy as np
class TestPermutation(unittest.TestCase):
    def test_autogenerated_fields(self):
        alphabet = [1,2,3,4,5]
        env = permutation_env.PermutationEnvironment(alphabet)
        self.assertEqual(120, env.number_of_possible_permutations)
        self.assertTrue(np.array_equal(env.permutations_added_array, np.array([0]*120)))